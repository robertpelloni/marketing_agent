def verify_build():
    try:
        r = subprocess.run(
            ["go", "build", "-buildvcs=false", "./cmd/tormentnexus"],
            capture_output=True,
            text=True,
            cwd=str(GO_DIR),
            timeout=120,
            encoding="utf-8",
            errors="replace",
            creationflags=subprocess.CREATE_NO_WINDOW,
        )
        if r.returncode == 0:
            log.build("CLEAN")
            log._inc("builds_ok")
            return True
        broken = set()
        undefs = set()
        for line in (r.stderr or "").split("\n"):
            for sep in ["internal\tools\\", "internal/tools/"]:
                if sep in line:
                    fname = line.split(sep, 1)[1].split(":")[0]
                    if fname.endswith(".go") and fname not in PROTECTED_FILES:
                        broken.add(fname)
                    break
            if "undefined:" in line:
                for w in line.split():
                    w = w.rstrip(",")
                    if w.startswith("Handle") and len(w) > 6:
                        undefs.add(w)
        for f in broken:
            p = TOOLS_DIR / f
            if p.exists():
                log.warn(f"Removing broken: {f}")
                p.unlink()
        if undefs:
            _clean_registry(undefs)
        log._inc("builds_fix")
        return False
    except Exception as e:
        log.err(f"Build error: {e}")
        return False


