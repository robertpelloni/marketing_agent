def name_to_filename(name):
    n = name
    for p in ["mcp-server-", "mcp-", "@", "server-", "mcp_"]:
        if n.lower().startswith(p):
            n = n[len(p) :]
    n = re.sub(r"[/.\-]+", "_", n)
    n = re.sub(r"[^a-zA-Z0-9_]", "", n).lower()
    if len(n) < 2:
        n = f"tool_{abs(hash(name)) % 10000}"
    return n[:60]


