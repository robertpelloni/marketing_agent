def fetch_source_code(github_url, temp_dir):
    """Download original MCP server source code to a temp directory.
    Returns dict of {filename: content} for key source files."""
    sources = {}
    if not github_url or "github.com" not in github_url:
        return sources

    repo_url = github_url.rstrip("/")
    if repo_url.startswith("git+"):
        repo_url = repo_url[4:]
    if repo_url.startswith("git@"):
        repo_url = "https://" + repo_url.split("@")[1].replace(":", "/")
    if repo_url.endswith(".git"):
        repo_url = repo_url[:-4]

    repo_name = repo_url.split("/")[-1]
    dest = os.path.join(temp_dir, repo_name)

    # Try shallow clone first
    try:
        subprocess.run(
            ["git", "clone", "--depth", "1", "--single-branch", repo_url, dest],
            capture_output=True,
            text=True,
            timeout=60,
            creationflags=subprocess.CREATE_NO_WINDOW,
        )
    except Exception:
        # Fall back to raw file fetching
        raw_base = repo_url.replace("github.com", "raw.githubusercontent.com")
        for branch in ["main", "master"]:
            for path in [
                "package.json",
                "mcp.json",
                "index.ts",
                "index.js",
                "server.ts",
                "server.py",
                "src/index.ts",
                "src/index.js",
                "src/server.ts",
                "src/server.py",
            ]:
                try:
                    req = urllib.request.Request(
                        f"{raw_base}/{branch}/{path}",
                        headers={"User-Agent": "Mozilla/5.0"},
                    )
                    with urllib.request.urlopen(req, timeout=15) as r:
                        content = r.read().decode("utf-8", errors="replace")
                        if content.strip():
                            sources[path] = content[:15000]
                except Exception:
                    continue
            if sources:
                break
        return sources

    # Read key source files from cloned repo
    if os.path.isdir(dest):
        ext_patterns = (".ts", ".js", ".py", ".json", ".md")
        for root, dirs, files in os.walk(dest):
            # Skip node_modules, .git, dist
            dirs[:] = [
                d
                for d in dirs
                if d not in ("node_modules", ".git", "dist", ".next", "build")
            ]
            for fn in files:
                if (
                    fn.endswith(ext_patterns)
                    and os.path.getsize(os.path.join(root, fn)) < 50000
                ):
                    rel = os.path.relpath(os.path.join(root, fn), dest)
                    try:
                        with open(
                            os.path.join(root, fn),
                            "r",
                            encoding="utf-8",
                            errors="replace",
                        ) as f:
                            sources[rel] = f.read()[:15000]
                    except Exception:
                        pass

    return sources


