BLACKLISTED_MODELS = set()


class LLMClient:
    def __init__(self):
        self.lock = threading.Lock()
        self.circuit_open = False
        self.circuit_opened_at = 0
        self.consecutive_fails = 0
        self.provider_idx = 0
        self.provider_last_call = {}
        self.provider_fails = {}
        self.provider_oks = {}
        # Global concurrency limiter for proxy calls
        self._llm_semaphore = threading.Semaphore(5)  # max 5 concurrent LLM requests

    def _next_provider(self):
        with self.lock:
            providers = DIRECT_PROVIDERS
            if not providers:
                return None
            now = time.time()
            for _ in range(len(providers)):
                p = providers[self.provider_idx % len(providers)]
                self.provider_idx += 1
                name = p["name"]
                cooldown = p.get("cooldown", 10)
                last = self.provider_last_call.get(name, 0)
                if now - last >= cooldown:
                    return p
            best = min(
                providers,
                key=lambda p: time.time() - self.provider_last_call.get(p["name"], 0),
            )
            return best

    def blacklist_model(self, model):
        BLACKLISTED_MODELS.add(model)
        log.warn(f"Blacklisted: {model} ({len(BLACKLISTED_MODELS)} total)")

    def _check_circuit(self):
        if not self.circuit_open:
            return True
        if time.time() - self.circuit_opened_at > CIRCUIT_BREAKER_COOLDOWN:
            log.stat("Circuit breaker reset")
            self.circuit_open = False
            self.consecutive_fails = 0
            return True
        return False

    def _trip_circuit(self):
        self.circuit_open = True
        self.circuit_opened_at = time.time()
        log.warn(f"Circuit OPEN - {CIRCUIT_BREAKER_COOLDOWN}s cooldown")

    def _record_fail(self, provider_name):
        self.consecutive_fails += 1
        self.provider_fails[provider_name] = (
            self.provider_fails.get(provider_name, 0) + 1
        )
        if self.consecutive_fails >= CIRCUIT_BREAKER_FAILS:
            self._trip_circuit()

    def _record_ok(self, provider_name):
        self.consecutive_fails = 0
        self.provider_fails[provider_name] = 0
        self.provider_oks[provider_name] = self.provider_oks.get(provider_name, 0) + 1

    def _wait_for_cooldown(self, provider):
        name = provider["name"]
        cooldown = provider.get("cooldown", 10)
        last = self.provider_last_call.get(name, 0)
        wait = cooldown - (time.time() - last)
        if wait > 0:
            time.sleep(wait)

    def call_provider(self, prompt, system, provider, wid=0):
        name = provider["name"]
        url = provider["url"]
        key = provider["key"]
        model = provider["model"]
        timeout = provider.get("timeout", 300)
        # Limit concurrent LLM calls to avoid overwhelming the proxy
        self._llm_semaphore.acquire()
        try:
            return self._call_provider_inner(prompt, system, provider, wid)
        finally:
            self._llm_semaphore.release()

    def _call_provider_inner(self, prompt, system, provider, wid=0):
        name = provider["name"]
        url = provider["url"]
        key = provider["key"]
        model = provider["model"]
        timeout = provider.get("timeout", 3600)
        self._wait_for_cooldown(provider)
        self.provider_last_call[name] = time.time()
        payload = json.dumps(
            {
                "model": model,
                "messages": [
                    {"role": "system", "content": system},
                    {"role": "user", "content": prompt},
                ],
                "temperature": 0.2,
                "max_tokens": 4096,
                "stream": True,
            }
        )
        with tempfile.NamedTemporaryFile(
            mode="w", suffix=".json", delete=False, encoding="utf-8"
        ) as tf:
            tf.write(payload)
            tf_path = tf.name
        t0 = time.time()
        log.llm(f"{name}/{model} t={timeout}s", wid)
        try:
            headers = ["-H", "Content-Type: application/json"]
            if key:
                headers += ["-H", f"Authorization: Bearer {key}"]
            result = subprocess.run(
                ["curl", "-s", "-N", "--max-time", str(timeout), "-X", "POST", url]
                + headers
                + ["--data", f"@{tf_path}"],
                capture_output=True,
                text=True,
                timeout=timeout + 30,
                encoding="utf-8",
                errors="replace",
                creationflags=subprocess.CREATE_NO_WINDOW,
            )
            elapsed = time.time() - t0
            # Parse SSE stream from proxy
            content = ""
            model_used = ""
            for line in result.stdout.split("\n"):
                line = line.strip()
                if line.startswith("data: ") and line != "data: [DONE]":
                    try:
                        d = json.loads(line[6:])
                        if not model_used:
                            model_used = d.get("model", model)
                        c = (
                            d.get("choices", [{}])[0]
                            .get("delta", {})
                            .get("content", "")
                        )
                        if c:
                            content += c
                    except:
                        pass
            if content and len(content) >= MIN_CONTENT_LENGTH:
                self._record_ok(name)
                log.llm(f"{name}: {len(content)}c in {elapsed:.1f}s", wid)
                log.record_model(name, True, len(content))
                return content, name, elapsed
            log.llm_fail(
                f"{name}: short/empty ({len(content)}c in {elapsed:.1f}s)", wid
            )
            self._record_fail(name)
            log.record_model(name, False)
            return None, name, elapsed
        except subprocess.TimeoutExpired:
            elapsed = time.time() - t0
            log.llm_fail(f"{name}: timeout", wid)
            self._record_fail(name)
            return None, name, elapsed
        except Exception as e:
            elapsed = time.time() - t0
            log.llm_fail(f"{name}: {str(e)[:120]}", wid)
            self._record_fail(name)
            return None, name, elapsed
        finally:
            try:
                os.unlink(tf_path)
            except:
                pass

    def call(self, prompt, system="", phase="gen", wid=0, max_retries=None):
        if not self._check_circuit():
            return None
        retries = max_retries or MAX_RETRIES
        for attempt in range(retries):
            if self.circuit_open:
                time.sleep(CIRCUIT_BREAKER_COOLDOWN)
                if not self._check_circuit():
                    continue
            provider = self._next_provider()
            if not provider:
                log.warn("No providers available!", wid)
                time.sleep(30)
                continue
            content, pname, elapsed = self.call_provider(prompt, system, provider, wid)
            if content:
                return content
            delay = RETRY_BASE_DELAY * (2 ** min(attempt, 3))
            log.warn(
                f"Retry {attempt + 1}/{retries} after {delay}s (provider: {pname})", wid
            )
            time.sleep(delay)
        log.err(f"All {retries} retries exhausted", wid)
        return None


