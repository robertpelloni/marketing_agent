def _strip_fences(code):
    code = re.sub(r"^```go\s*\n?", "", code)
    code = re.sub(r"\n?```\s*$", "", code)
    return code.strip()


def _has_handlers(code):
    return bool(re.search(r"func Handle\w+\s*\(", code))


def _parse_manifest(raw, go_filename):
    raw = _strip_fences(raw)
    try:
        return json.loads(raw)
    except:
        pass
    fixed = re.sub(r",\s*}", "}", raw)
    fixed = re.sub(r",\s*]", "]", fixed)
    try:
        return json.loads(fixed)
    except:
        return None


def _assemble_from_blocks(blocks):
    code_blocks = [b.strip() for b in blocks if b.strip()]
    if not code_blocks:
        return None
    for block in code_blocks:
        if "package tools" in block and _has_handlers(block):
            return block
    base = None
    additions = []
    for block in code_blocks:
        if "package tools" in block:
            base = block
        elif _has_handlers(block) or re.match(r"(type|var|const)\s", block):
            additions.append(block)
    if base:
        for add in additions:
            if add not in base:
                fh = re.search(r"func Handle\w+", base)
                if fh:
                    base = base[: fh.start()] + add + "\n\n" + base[fh.start() :]
                else:
                    base += "\n\n" + add
        return base
    if additions:
        imports = set()
        for block in additions:
            for imp in re.findall(r'"([^"]+)"', block):
                if "/" not in imp or imp.startswith("golang.org"):
                    imports.add(imp)
        imp_block = (
            ("import (\n" + "\n".join(f'\t"{i}"' for i in sorted(imports)) + "\n)\n\n")
            if imports
            else ""
        )
        code = "package tools\n\n" + imp_block + "\n\n".join(additions)
        if _has_handlers(code):
            return code
    return None


def _extract_from_mixed(code):
    idx = code.find("package tools")
    if idx < 0:
        return None
    return _clean_raw_code(code[idx:])


def _clean_raw_code(code):
    lines = code.split("\n")
    clean = []
    in_code = False
    skip = False
    for line in lines:
        s = line.strip()
        if re.match(r"^\d+\.\s+\*\*", s):
            skip = True
            continue
        if re.match(r"^[*\-]\s+\*\*", s):
            skip = True
            continue
        if skip:
            if s.startswith(
                ("package ", "import ", "func ", "type ", "var ", "const ", "//")
            ) or (s == "" and in_code):
                skip = False
            else:
                continue
        if s.startswith("package tools"):
            in_code = True
        if in_code:
            clean.append(line)
    result = "\n".join(clean)
    return result if _has_handlers(result) else None


def fix_truncated_code(code):
    if not code:
        return code
    lines = code.rstrip().split("\n")
    while lines:
        s = lines[-1].strip()
        if not s:
            break
        if re.search(r"[;,{}()\[\]]\s*$", s) or s.endswith(")"):
            break
        lines.pop()
    code = "\n".join(lines)
    open_b = code.count("{") - code.count("}")
    if open_b > 0:
        for i in range(open_b):
            code += "\n" + "\t" * max(0, open_b - i - 1) + "}"
    for hm in re.finditer(r"func (Handle\w+)\s*\([^)]*\)\s*\([^)]*\)\s*\{", code):
        start = hm.end()
        depth = 1
        end = start
        for i in range(start, len(code)):
            if code[i] == "{":
                depth += 1
            elif code[i] == "}":
                depth -= 1
            if depth == 0:
                end = i
                break
        body = code[start:end]
        if "return ok(" not in body and "return err(" not in body:
            lb = code.rfind("}", start, end + 1)
            if lb > 0:
                code = code[:lb] + '\n\treturn ok("not yet implemented")\n' + code[lb:]
                break
    return code


def _build_manifest_from_code(code, go_filename):
    handlers = re.findall(r"func (Handle\w+)\s*\(", code)
    return {
        "filename": f"{go_filename}.go",
        "handlers": [
            {
                "tool_name": re.sub(r"([A-Z])", r"_\1", h[6:]).lower().lstrip("_"),
                "handler_func": h,
                "description": h,
            }
            for h in handlers
        ],
    }


# === REVIEW PARSER ===
def parse_review(content):
    if not content:
        return False, ["empty review"]
    jm = re.search(r'\{[^{}]*"compiles"\s*:\s*(true|false)[^{}]*\}', content, re.DOTALL)
    if jm:
        try:
            d = json.loads(jm.group(0))
            return d.get("compiles", False), d.get("errors", [])
        except:
            pass
    cl = content.lower()
    if '"compiles": true' in cl or '"compiles":true' in cl:
        return True, []
    if '"compiles": false' in cl or '"compiles":false' in cl:
        errors = re.findall(r'"errors"\s*:\s*\[(.*?)\]', content, re.DOTALL)
        err_list = (
            re.findall(r'"([^"]+)"', errors[0]) if errors else ["reviewer found errors"]
        )
        return False, err_list
    return False, ["unparseable review"]


