# === LOG ===
class Log:
    C = {
        "R": "\033[91m",
        "G": "\033[92m",
        "Y": "\033[93m",
        "B": "\033[94m",
        "M": "\033[95m",
        "C": "\033[96m",
        "0": "\033[0m",
    }

    def __init__(self, path=None):
        self.lock = threading.Lock()
        self.path = path
        self.t0 = time.time()
        self.ok_count = 0
        self.fail_count = 0
        self.llm_ok_count = 0
        self.llm_err_count = 0
        self.builds_ok = 0
        self.builds_fix = 0
        self.model_stats = {}
        self.phase_stats = {
            "gen_ok": 0,
            "gen_err": 0,
            "rev_ok": 0,
            "rev_err": 0,
            "fix_ok": 0,
            "fix_err": 0,
            "fix_needed": 0,
            "fix_compile_ok": 0,
            "fix_compile_err": 0,
            "rev_agreed": 0,
        }

    def _ts(self):
        e = int(time.time() - self.t0)
        return f"{e // 3600:02d}:{(e % 3600) // 60:02d}:{e % 60:02d}"

    def _w(self, lvl, col, wid, msg):
        p = (
            f"[{self._ts()}]"
            + (f"[W{wid}]" if wid is not None else "")
            + f"[{lvl}] {msg}"
        )
        with self.lock:
            print(f"{self.C.get(col, '')}{p}{self.C['0']}", flush=True)
            if self.path:
                try:
                    with open(self.path, "a", encoding="utf-8", errors="replace") as f:
                        f.write(p + "\n")
                except:
                    pass

    def info(self, m, w=None):
        self._w("INFO", "C", w, m)

    def ok(self, m, w=None):
        self._w("OK", "G", w, m)
        self._inc("ok_count")

    def warn(self, m, w=None):
        self._w("WARN", "Y", w, m)

    def err(self, m, w=None):
        self._w("ERR", "R", w, m)
        self._inc("fail_count")

    def llm(self, m, w=None):
        self._w("LLM", "M", w, m)
        self._inc("llm_ok_count")

    def llm_fail(self, m, w=None):
        self._w("LLM-ERR", "R", w, m)
        # self._inc("llm_err_count")  # disabled

    def phase(self, m, w=None):
        self._w("PHASE", "B", w, m)

    def stat(self, m):
        self._w("STAT", "B", None, m)

    def build(self, m):
        self._w("BUILD", "B", None, m)

    def _inc(self, k):
        with self.lock:
            setattr(self, k, getattr(self, k) + 1)

    def record_model(self, model, ok, chars=0):
        with self.lock:
            s = self.model_stats.setdefault(model, {"ok": 0, "err": 0, "chars": 0})
            if ok:
                s["ok"] += 1
                s["chars"] += chars
            else:
                s["err"] += 1

    def record_phase(self, phase, ok):
        with self.lock:
            k = f"{phase}_ok" if ok else f"{phase}_err"
            if k in self.phase_stats:
                self.phase_stats[k] += 1

    def summary(self):
        e = int(time.time() - self.t0)
        with self.lock:
            ps = self.phase_stats
            s = (
                f"ok={self.ok_count} fail={self.fail_count} "
                f"llm_ok={self.llm_ok_count} llm_err={self.llm_err_count} "
                f"gen={ps['gen_ok']}/{ps['gen_err']} "
                f"rev={ps['rev_ok']}/{ps['rev_err']} "
                f"fix={ps['fix_ok']}/{ps['fix_err']} "
                f"fix_needed={ps['fix_needed']} rev_agree={ps['rev_agreed']} "
                f"elapsed={e // 3600}h{(e % 3600) // 60}m"
            )
            best = sorted(
                [(m, v["ok"]) for m, v in self.model_stats.items() if v["ok"] > 0],
                key=lambda x: x[1],
                reverse=True,
            )[:3]
            if best:
                s += " | best: " + ", ".join(f"{m}({o})" for m, o in best)
            return s


