class ProxyManager:
    def __init__(self):
        self._healthy = True

    def check(self):
        for attempt in range(3):
            try:
                import urllib.request

                with urllib.request.urlopen(f"{PROXY_URL}/v1/models", timeout=5) as r:
                    if r.status == 200:
                        self._healthy = True
                        return True
            except:
                if attempt < 2:
                    time.sleep(2)
        self._healthy = False
        return False

    def restart(self):
        log.stat("Restarting proxy...")
        try:
            subprocess.run(
                ["taskkill", "/F", "/IM", "freellm.exe"],
                capture_output=True,
                timeout=10,
                creationflags=subprocess.CREATE_NO_WINDOW,
            )
        except Exception as e:
            log.warn(f"taskkill failed: {e}")
        time.sleep(5)
        try:
            normalized_bin = os.path.normpath(PROXY_BIN)
            d = os.path.dirname(normalized_bin)
            log.stat(f"Spawning proxy: {normalized_bin} in cwd: {d}")
            subprocess.Popen(
                [normalized_bin],
                cwd=d,
                stdout=subprocess.DEVNULL,
                stderr=subprocess.DEVNULL,
                creationflags=subprocess.CREATE_NO_WINDOW,
            )
        except Exception as e:
            log.err(f"Proxy start: {e}")
            return False
        for i in range(24):
            time.sleep(5)
            if self.check():
                log.ok("Proxy back up!")
                return True
        log.err("Proxy never came up")
        return False

    @property
    def healthy(self):
        return self._healthy


