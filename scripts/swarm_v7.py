_merge_lock = threading.Lock()


def merge_manifests():
    with _merge_lock:
        if not MANIFEST_DIR.exists():
            return 0
        mfs = list(MANIFEST_DIR.glob("*.json"))
        if not mfs:
            return 0
        all_h = []
        for mf in mfs:
            try:
                with open(mf, "r", encoding="utf-8") as f:
                    data = json.load(f)
                for h in data.get("handlers", []):
                    tn = h.get("tool_name", "")
                    hf = h.get("handler_func", "")
                    if tn and hf:
                        all_h.append((tn, hf))
                mf.unlink()
            except:
                mf.unlink()
        if not all_h:
            return 0
        try:
            with open(REGISTRY_FILE, "r", encoding="utf-8", errors="replace") as f:
                content = f.read()
        except:
            return 0
        existing = set(re.findall(r'r\.handlers\["([^"]+)"\]', content))
        to_add = []
        added = 0
        for tn, hf in all_h:
            if tn not in existing and hf not in content:
                to_add.append(f'\tr.handlers["{tn}"] = {hf}')
                added += 1
                existing.add(tn)
        if not added:
            return 0
        insert = "\n".join(to_add) + "\n"
        lines = content.split("\n")
        idx = -1
        depth = 0
        in_func = False
        for i, line in enumerate(lines):
            if "func (r *Registry) registerAll()" in line:
                in_func = True
                depth = 0
            if in_func:
                depth += line.count("{") - line.count("}")
                if depth <= 0 and i > 0:
                    idx = i
                    in_func = False
                    break
        if idx < 0:
            for i in range(len(lines) - 1, -1, -1):
                if lines[i].strip() == "}":
                    idx = i
                    break
        if idx >= 0:
            lines.insert(idx, insert)
            with open(REGISTRY_FILE, "w", encoding="utf-8") as f:
                f.write("\n".join(lines))
            log.ok(f"Merged {added} handlers")
        return added


# === WORKER — 3-Phase Pipeline ===
def worker_loop(worker_id, shutdown, completed_lock, completed_count):
    stagger = (worker_id - 1) * 5  # 5s between workers (50 workers = ~4min stagger)
    if stagger > 0:
        log.info(f"Staggering {stagger}s", worker_id)
        for _ in range(stagger):
            if shutdown.is_set():
                return
            time.sleep(1)

    while not shutdown.is_set():
        task = claim_task(worker_id)
        if not task:
            log.info("No more tasks", worker_id)
            return
        name = task["name"]
        fn = name_to_filename(name)
        log.info(f"Claimed: {name} -> {fn}.go", worker_id)

        # === PHASE 1: GENERATE ===
        log.phase(f"GEN: {name}", worker_id)

        # Fetch original source code if GitHub URL is available
        sources = {}
        github_url = task.get("github_url", "")
        if github_url and "github.com" in github_url:
            log.info(f"Fetching source for {name}: {github_url}", worker_id)
            temp_dir = tempfile.mkdtemp(prefix="mcp_src_")
            try:
                sources = fetch_source_code(github_url, temp_dir)
                if sources:
                    log.info(f"Got {len(sources)} source files for {name}", worker_id)
                else:
                    log.warn(f"No source files fetched for {name}", worker_id)
            except Exception as e:
                log.warn(f"Source fetch failed for {name}: {e}", worker_id)
            finally:
                # Clean up temp dir
                try:
                    import shutil

                    shutil.rmtree(temp_dir, ignore_errors=True)
                except Exception:
                    pass

        prompt, system = make_gen_prompt(task, sources=sources if sources else None)
        output = llm.call(prompt, system, phase="gen", wid=worker_id)
        if not output:
            fail_task(name, "gen: LLM failed")
            log.err(f"GEN FAIL: {name}", worker_id)
            log.record_phase("gen", False)
            continue
        go_code, manifest = extract_go_code(output, fn)
        if not go_code or "package tools" not in go_code:
            fail_task(name, "gen: no code")
            log.err(f"GEN NO-CODE: {name}", worker_id)
            log.record_phase("gen", False)
            continue
        if "package main" in go_code:
            fail_task(name, "gen: package main")
            log.record_phase("gen", False)
            continue
        if go_code.count("func (ctx context.Context)") > 3:
            fail_task(name, "gen: garbage")
            log.record_phase("gen", False)
            continue
        if "type ToolResponse struct" in go_code:
            go_code = re.sub(
                r"type ToolResponse struct\s*\{.*?\}", "", go_code, flags=re.DOTALL
            )
            go_code = re.sub(
                r"type TextContent struct\s*\{.*?\}", "", go_code, flags=re.DOTALL
            )
        external = re.findall(r'"(github\.com/(?!tormentnexushq)[^"]+)"', go_code)
        if external:
            go_code = re.sub(
                r'\s*"[^"]*github\.com/(?!tormentnexushq)[^"]*"\s*\n?', "\n", go_code
            )
            log.warn(f"GEN: stripped {len(external)} external imports", worker_id)
        handlers_found = re.findall(r"func (Handle\w+)\s*\(", go_code)
        if not handlers_found:
            fail_task(name, "gen: no handlers")
            log.err(f"GEN NO-HANDLER: {name}", worker_id)
            log.record_phase("gen", False)
            continue
        log.ok(f"GEN OK: {name} ({len(handlers_found)} handlers)", worker_id)
        log.record_phase("gen", True)
        time.sleep(PIPELINE_COOLDOWN)

        # === PHASE 2: REVIEW (2 independent reviewers) ===
        log.phase(f"REV: {name} (x2)", worker_id)
        rev_prompt, rev_system = make_review_prompt(go_code)
        reviews = []
        for rev_num in range(2):
            review_output = llm.call(
                rev_prompt, rev_system, phase="rev", wid=worker_id, max_retries=3
            )
            if review_output:
                compiles, errors = parse_review(review_output)
                reviews.append({"compiles": compiles, "errors": errors})
                log.info(
                    f"REV-{rev_num + 1}: compiles={compiles} err={errors[:3]}",
                    worker_id,
                )
                log.record_phase("rev", True)
            else:
                reviews.append({"compiles": False, "errors": ["review LLM failed"]})
                log.record_phase("rev", False)
            if rev_num == 0:
                time.sleep(PIPELINE_COOLDOWN)

        both_compile = all(r["compiles"] for r in reviews)
        any_compile = any(r["compiles"] for r in reviews)
        all_errors = list(
            dict.fromkeys(e for r in reviews for e in r.get("errors", []))
        )
        if both_compile:
            log.ok(f"REV PASS: {name}", worker_id)
            with log.lock:
                log.phase_stats["rev_agreed"] += 1
        elif any_compile:
            log.warn(f"REV SPLIT: {name} errors={all_errors[:3]}", worker_id)
        else:
            log.warn(f"REV FAIL: {name} errors={all_errors[:3]}", worker_id)

        # === PHASE 3: FIX (if any reviewer found errors) ===
        final_code = go_code
        review_status = "pass"
        if not both_compile and all_errors:
            log.phase(f"FIX: {name} ({len(all_errors)} errors)", worker_id)
            with log.lock:
                log.phase_stats["fix_needed"] += 1
            time.sleep(PIPELINE_COOLDOWN)
            fix_prompt, fix_system = make_fix_prompt(go_code, all_errors)
            fix_output = llm.call(fix_prompt, fix_system, phase="fix", wid=worker_id)
            if fix_output:
                fixed_code, _ = extract_go_code(fix_output, fn)
                if (
                    fixed_code
                    and "package tools" in fixed_code
                    and _has_handlers(fixed_code)
                ):
                    ext2 = re.findall(
                        r'"(github\.com/(?!tormentnexushq)[^"]+)"', fixed_code
                    )
                    if ext2:
                        fixed_code = re.sub(
                            r'\s*"[^"]*github\.com/(?!tormentnexushq)[^"]*"\s*\n?',
                            "\n",
                            fixed_code,
                        )
                    if "type ToolResponse struct" in fixed_code:
                        fixed_code = re.sub(
                            r"type ToolResponse struct\s*\{.*?\}",
                            "",
                            fixed_code,
                            flags=re.DOTALL,
                        )
                        fixed_code = re.sub(
                            r"type TextContent struct\s*\{.*?\}",
                            "",
                            fixed_code,
                            flags=re.DOTALL,
                        )
                    fixed_handlers = re.findall(r"func Handle\w+\s*\(", fixed_code)
                    if fixed_handlers:
                        # Verify the fixed code has balanced braces and no truncation
                        ob = fixed_code.count("{")
                        cb = fixed_code.count("}")
                        if ob != cb:
                            log.warn(
                                f"FIX TRUNCATED: {name} (braces {ob}/{cb}), keeping original",
                                worker_id,
                            )
                            review_status = "fix-truncated-keeporig"
                            log.record_phase("fix", False)
                        else:
                            final_code = fixed_code
                            handlers_found = fixed_handlers
                            review_status = "fixed"
                            log.ok(f"FIX OK: {name}", worker_id)
                            log.record_phase("fix", True)
                    else:
                        review_status = "fix-nohandler-keeporig"
                        log.record_phase("fix", False)
                else:
                    review_status = "fix-nocode-keeporig"
                    log.record_phase("fix", False)
            else:
                review_status = "fix-llmfail-keeporig"
                log.record_phase("fix", False)
        else:
            log.record_phase("fix", True)

        # === SANITIZE CODE ===
        final_code = sanitize_go_code(final_code)

        # === WRITE FILE (with Go compilation check) ===
        go_file = f"{fn}.go"
        # Write to a staging location first
        staging_dir = TOOLS_DIR / "_staging"
        staging_dir.mkdir(exist_ok=True)
        staging_path = staging_dir / go_file
        try:
            with open(staging_path, "w", encoding="utf-8") as f:
                f.write(final_code)
        except Exception as e:
            fail_task(name, f"write error: {e}")
            log.err(f"Write {go_file}: {e}", worker_id)
            continue

        # === ITERATIVE COMPILE CHECK (up to 3 fix-attempts with real Go compiler errors) ===
        # This prevents corrupted .go files from landing in the real tools/ dir.
        compile_ok = False
        compile_errors = ""
        for compile_attempt in range(3):
            # Write the current code to staging
            with open(staging_path, "w", encoding="utf-8") as f:
                f.write(final_code)
            # Copy registry + server so the package can compile
            try:
                for dep in ("registry.go", "server.go"):
                    dep_src = TOOLS_DIR / dep
                    if dep_src.exists():
                        (staging_dir / dep).write_text(
                            dep_src.read_text(encoding="utf-8"), encoding="utf-8"
                        )
                result = subprocess.run(
                    ["go", "build", "./internal/tools/_staging"],
                    capture_output=True,
                    text=True,
                    timeout=30,
                    cwd=str(GO_DIR),
                )
                if result.returncode == 0:
                    compile_ok = True
                    break
                # Extract real compiler errors
                compile_errors = result.stderr[:1000]
                log.warn(
                    f"Compile fail (attempt {compile_attempt + 1}): {compile_errors[:200]}",
                    worker_id,
                )
            except subprocess.TimeoutExpired:
                log.warn(
                    f"Compile timed out (attempt {compile_attempt + 1}), accepting",
                    worker_id,
                )
                compile_ok = True
                break
            except FileNotFoundError:
                log.warn("go not found on PATH, skipping compile check", worker_id)
                compile_ok = True
                break

            if compile_attempt < 2:
                # Feed REAL compiler errors back to LLM for fixing
                log.phase(
                    f"FIX-COMPILE: {name} (attempt {compile_attempt + 1})", worker_id
                )
                fix_prompt, fix_system = make_compile_fix_prompt(
                    final_code, compile_errors
                )
                fix_output = llm.call(
                    fix_prompt,
                    fix_system,
                    phase="fix-compile",
                    wid=worker_id,
                    max_retries=2,
                )
                if fix_output:
                    fixed_code, _ = extract_go_code(fix_output, fn)
                    if (
                        fixed_code
                        and "package tools" in fixed_code
                        and _has_handlers(fixed_code)
                    ):
                        # Clean external imports
                        ext = re.findall(
                            r'"(github\.com/(?!tormentnexushq)[^"]+)"', fixed_code
                        )
                        if ext:
                            fixed_code = re.sub(
                                r'\s*"[^"]*github\.com/(?!tormentnexushq)[^"]*"\s*\n?',
                                "\n",
                                fixed_code,
                            )
                        if "type ToolResponse struct" in fixed_code:
                            fixed_code = re.sub(
                                r"type ToolResponse struct\s*\{.*?\}",
                                "",
                                fixed_code,
                                flags=re.DOTALL,
                            )
                            fixed_code = re.sub(
                                r"type TextContent struct\s*\{.*?\}",
                                "",
                                fixed_code,
                                flags=re.DOTALL,
                            )
                        ob = fixed_code.count("{")
                        cb = fixed_code.count("}")
                        if ob == cb:
                            final_code = fixed_code
                            log.ok(
                                f"Compile fix attempt {compile_attempt + 1}: updated code",
                                worker_id,
                            )
                        else:
                            log.warn(
                                f"Compile fix truncated (braces {ob}/{cb}), keeping previous",
                                worker_id,
                            )
                    else:
                        log.warn(
                            "Compile fix produced invalid code, keeping previous",
                            worker_id,
                        )
                else:
                    log.warn("Compile fix LLM failed, keeping previous", worker_id)
                time.sleep(1)

        if not compile_ok:
            log.warn(
                f"Compile FAILED after 3 attempts for {go_file}: {compile_errors[:200]}",
                worker_id,
            )
            # Write to broken directory
            broken_fix_dir = TOOLS_DIR / "_broken"
            broken_fix_dir.mkdir(exist_ok=True)
            (broken_fix_dir / go_file).write_text(final_code, encoding="utf-8")
            staging_path.unlink(missing_ok=True)
            fail_task(name, f"compile failed after 3 tries: {compile_errors[:200]}")
            continue

        # Compilation passed — promote from staging to real tools/
        if staging_path.exists():
            shutil.copy2(staging_path, TOOLS_DIR / go_file)
            staging_path.unlink(missing_ok=True)
            log.ok(f"Compile check PASSED for {go_file}", worker_id)
    if manifest:
        MANIFEST_DIR.mkdir(exist_ok=True)
        try:
            with open(MANIFEST_DIR / f"{fn}.json", "w", encoding="utf-8") as f:
                json.dump(manifest, f, indent=2)
        except:
            pass
        tc = len(handlers_found)
        complete_task(name, go_file, tc, review_status)
        log.ok(f"DONE: {name} ({tc}h, {review_status})", worker_id)
    with completed_lock:
        completed_count[0] += 1


# === WORKER — Validation Phase ===
def worker_validate(worker_id, shutdown, completed_lock, completed_count):
    stagger = (worker_id - 1) * 3
    if stagger:
        time.sleep(stagger)

    while not shutdown.is_set():
        task = claim_implemented_task(worker_id)
        if not task:
            log.info("No more servers to validate", worker_id)
            return
        name = task["name"]
        github_url = task["github_url"]

        # Find the Go file
        fn = name_to_filename(name) + ".go"
        go_path = TOOLS_DIR / fn
        if not go_path.exists():
            # Try fuzzy match
            matches = list(TOOLS_DIR.glob("*.go"))
            go_code = ""
            for m in matches:
                if name.lower()[:15] in m.stem.lower():
                    go_code = m.read_text(encoding="utf-8", errors="replace")
                    fn = m.name
                    break
            if not go_code:
                record_validation(name, "MISSING", f"no Go file for {name}")
                with completed_lock:
                    completed_count[0] += 1
                continue
        else:
            go_code = go_path.read_text(encoding="utf-8", errors="replace")

        if not go_code.strip() or "//go:build ignore" in go_code:
            record_validation(name, "STUB", "build-constrained stub, no implementation")
            with completed_lock:
                completed_count[0] += 1
            continue

        # Fetch original MCP server info from GitHub
        original_info = ""
        if github_url and "github.com" in github_url:
            raw_base = github_url.rstrip("/").replace(
                "github.com", "raw.githubusercontent.com"
            )
            for branch in ["main", "master"]:
                for path in ["README.md", "package.json", "mcp.json"]:
                    try:
                        req = urllib.request.Request(
                            f"{raw_base}/{branch}/{path}",
                            headers={"User-Agent": "Mozilla/5.0"},
                        )
                        with urllib.request.urlopen(req, timeout=10) as r:
                            original_info += f"\n[{branch}/{path}]:\n{r.read().decode('utf-8', errors='replace')[:5000]}"
                    except Exception:
                        continue
                if original_info:
                    break

        if not original_info:
            record_validation(
                name, "NO_ORIGINAL", f"cannot fetch source from {github_url}"
            )
            with completed_lock:
                completed_count[0] += 1
            continue

        # Use LLM to validate
        log.phase(f"VALIDATE: {name}", worker_id)
        prompt = f"""You are validating a Go port of an MCP server.

Original MCP server ({name}):
Source: {github_url}
{original_info[:4000]}

Go port:
```go
{go_code[:4000]}
```

Is this a CLEAN_PORT, PARTIAL_PORT, MISMATCH, or STUB_ONLY?
Reply with exactly one keyword on the first line, then a brief explanation on the second line."""

        result = llm.call(prompt, "", phase="validate", wid=worker_id)
        if not result:
            record_validation(name, "LLM_FAIL", "LLM call failed")
            with completed_lock:
                completed_count[0] += 1
            continue

        lines = result.strip().split("\n")
        status = lines[0].strip().upper() if lines else "UNKNOWN"
        detail = lines[1].strip()[:100] if len(lines) > 1 else ""

        for valid in ["CLEAN_PORT", "PARTIAL_PORT", "MISMATCH", "STUB_ONLY"]:
            if valid in status:
                status = valid
                break
        else:
            status = "UNKNOWN"
            detail = result[:100]

        record_validation(name, status, detail)
        if status == "CLEAN_PORT":
            log.ok(f"VALIDATE OK: {name} -> {status}", worker_id)
        else:
            log.warn(f"VALIDATE: {name} -> {status} ({detail[:60]})", worker_id)

        with completed_lock:
            completed_count[0] += 1

    with completed_lock:
        completed_count[0] += 1


# === ORCHESTRATOR ===
class Swarm:
    def __init__(
        self, max_workers, limit, forever, repair_only, no_research, validate=False
    ):
        self.max_workers = max_workers
        self.limit = limit
        self.forever = forever
        self.repair_only = repair_only
        self.no_research = no_research
        self.validate = validate
        self.running = True
        self.shutdown = threading.Event()
        self.completed_lock = threading.Lock()
        self.completed_count = [0]
        signal.signal(signal.SIGINT, lambda s, f: self._stop())
        signal.signal(signal.SIGTERM, lambda s, f: self._stop())

    def _stop(self):
        log.stat("Shutdown signal...")
        self.running = False
        self.shutdown.set()

    def run(self):
        log.stat("=" * 70)
        log.stat(" TORMENTNEXUS SWARM v7 - THE VERIFIED ASSIMILATOR")
        log.stat("=" * 70)
        log.stat(
            f"Workers: {self.max_workers} | Limit: {self.limit} | Forever: {self.forever}"
        )
        if self.validate:
            log.stat("Pipeline: VALIDATE (compare Go ports vs original MCP servers)")
        else:
            log.stat("Pipeline: GENERATE -> REVIEW(2) -> FIX")
        log.stat(f"Generator models: {GENERATOR_MODELS[:3]}...")
        log.stat(f"Reviewer models: {REVIEWER_MODELS[:3]}...")
        log.stat(f"Fixer models: {FIXER_MODELS[:3]}...")
        t, i, p, f = get_stats()
        log.stat(f"DB: {t} total | {i} done | {p} pending | {f} failed")
        MANIFEST_DIR.mkdir(exist_ok=True)
        stale = reset_stale()
        if stale:
            log.info(f"Released {stale} stale tasks")
        if not proxy.check():
            log.warn("Proxy not healthy - restarting...")
            if not proxy.restart():
                log.err("Cannot start proxy!")
                return False
        if self.repair_only:
            repair_build()
            merge_manifests()
            verify_build()
            return True
        log.stat("Checking build...")
        repair_build()
        merge_manifests()
        verify_build()
        while self.running:
            try:
                self._run_wave()
            except Exception as e:
                log.err(f"Wave error: {e}")
                traceback.print_exc()
                if not self.forever:
                    break
                time.sleep(30)
            if not self.forever and self.completed_count[0] >= self.limit:
                log.stat(f"Limit reached ({self.limit})!")
                break
            _, _, pending, failed = get_stats()
            if pending == 0:
                if self.forever and failed > 0:
                    n = reset_failed(limit=50)
                    if n > 0:
                        log.stat(f"Retrying {n} failed tasks")
                        continue
                log.stat("No pending tasks!")
                if self.forever:
                    log.info("Waiting 60s for next cycle...")
                    time.sleep(60)
                    continue
                break
            if self.forever:
                n = reset_failed(limit=20)
                if n:
                    log.info(f"Auto-retry: {n} failed tasks")
                    time.sleep(5)
        self._shutdown()
        return True

    def _run_wave(self):
        ws = self.max_workers
        if not self.forever:
            ws = min(ws, max(self.limit - self.completed_count[0], 1))
        log.stat(f"Wave: {ws} workers")
        before = self.completed_count[0]
        with ThreadPoolExecutor(max_workers=ws) as pool:
            futs = {}
            for wid in range(1, ws + 1):
                if self.shutdown.is_set():
                    break
                futs[
                    pool.submit(
                        (worker_validate if self.validate else worker_loop),
                        wid,
                        self.shutdown,
                        self.completed_lock,
                        self.completed_count,
                    )
                ] = wid
            last_bc = 0
            for f in as_completed(futs):
                if self.shutdown.is_set():
                    break
                try:
                    f.result()
                except Exception as e:
                    log.err(f"Worker {futs[f]}: {e}")
                now = self.completed_count[0]
                if now - last_bc >= BUILD_VERIFY_EVERY:
                    merge_manifests()
                    repair_build()
                    last_bc = now
        merge_manifests()
        repair_build()
        delta = self.completed_count[0] - before
        t, i, p, f2 = get_stats()
        log.stat(
            f"Wave done: +{delta} | DB: {i}/{t} done, {p} pend, {f2} fail | {log.summary()}"
        )

    def _shutdown(self):
        log.stat("Shutting down...")
        merge_manifests()
        repair_build()
        n = _remove_external_imports()
        if n:
            log.info(f"Removed {n} files with external imports")
        repair_build()
        tc = len(list(TOOLS_DIR.glob("*.go")))
        try:
            hc = len(
                re.findall(
                    r"r\.handlers\[",
                    REGISTRY_FILE.read_text(encoding="utf-8", errors="replace"),
                )
            )
        except:
            hc = 0
        t, i, p, f = get_stats()
        log.stat("=" * 70)
        log.stat(" FINAL STATE")
        log.stat("=" * 70)
        log.stat(f" Tool files: {tc} | Handlers: {hc}")
        log.stat(f" DB: {i}/{t} done, {p} pending, {f} failed")
        log.stat(f" Pipeline: {log.phase_stats}")
        log.stat(f" {log.summary()}")
        log.stat("=" * 70)


